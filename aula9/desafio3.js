// ---Exercicios push, pop, shift e unshif

// ---Exercício 1: Adicionando elementos no final do array ( push ):
// ---Array inicial
const myArray = [1, 2, 3];
myArray.push(4);
console.log(myArray);

// Exercício 2: Removendo elementos do final do array ( pop ):
// // Array inicial
const myArray1 = [1, 2, 3, 4];
myArray1.pop();
console.log(myArray1);

// Exercício 3: Adicionando elementos no início do array ( unshift ):
// // Array inicial
const myArray2 = [2, 3, 4];
myArray2.unshift(0);
console.log(myArray2);

// Exercício 4: Removendo elementos do início do array ( shift ):
// // Array inicial
const myArray3 = [1, 2, 3, 4];
myArray3.shift();
console.log(myArray3);

// Exercício 5: Adicionando múltiplos elementos no final do array ( push ):
// // Array inicial
const myArray4 = [1, 2];
myArray4.push(3);
myArray4.push(4);
myArray4.push(5);
console.log(myArray4);
