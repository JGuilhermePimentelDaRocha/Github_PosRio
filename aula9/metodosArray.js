const nomes = ["João", "Maria", "José", "Luana"]

//-- O push()método adiciona um novo elemento a um array (no final) --//

nomes.push ("Guilherme")

console.log (nomes)

//-- O pop()método remove o último elemento de um array --//

nomes.pop ()

console.log (nomes)


// O unshift()método adiciona um novo elemento a um array (no início) e "desloca" os elementos mais antigos --//

nomes.unshift ("Ana")

console.log (nomes)

// -- O shift()método remove o primeiro elemento do array e “muda” todos os outros elementos para um índice inferior --//

nomes.shift ()

console.log (nomes)